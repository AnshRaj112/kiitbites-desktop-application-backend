const mongoose = require("mongoose");
const { Cluster_User } = require('../../config/db');

const ContactMessageSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true
    },
    message: {
        type: String,
        required: true,
        trim: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = Cluster_User.model("ContactMessage", ContactMessageSchema);